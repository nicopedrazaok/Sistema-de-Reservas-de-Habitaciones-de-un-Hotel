    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtNombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el Nombre del Producto");
            txtNombre.requestFocus();
            return;
        }
        if (txtDescripcion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una descripcion para el producto");
            txtDescripcion.requestFocus();
            return;
        }
        if (txtPrecioVenta.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el precio de venta del producto");
            txtPrecioVenta.requestFocus();
            return;
        }

        Producto producto = new Producto();
        GestorBDProducto gestor = new GestorBDProducto();

        producto.setNombre(txtNombre.getText());
        producto.setDescripcion(txtDescripcion.getText());
        int UnidadMedida = cboUnidadMedida.getSelectedIndex();
        producto.setUnidadMedida((String) cboUnidadMedida.getItemAt(UnidadMedida));
        producto.setPrecioVenta(Double.parseDouble(txtPrecioVenta.getText()));

        if (accion.equals("guardar")) {
            if (gestor.insertar(producto)) {
                JOptionPane.showMessageDialog(rootPane, "El Producto fue resgistrado correctamente");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            producto.setIdProducto(Integer.parseInt(txtIdProducto.getText()));
            if (gestor.editar(producto)) {
                JOptionPane.showMessageDialog(rootPane, "El Producto fue modificado correctamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

