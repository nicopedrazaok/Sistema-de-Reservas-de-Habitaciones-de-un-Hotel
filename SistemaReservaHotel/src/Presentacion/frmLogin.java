    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        try {
            DefaultTableModel modelo;
            GestorBDTrabajador gestor = new GestorBDTrabajador();
            Trabajador trabajador = new Trabajador();

            trabajador.setLogin(txtUsuario.getText());
            trabajador.setPassword(txtPassword.getText());

            modelo = gestor.login(trabajador.getLogin(), trabajador.getPassword());
            tblListado.setModel(modelo);
            if (gestor.totalRegistros > 0) {
                this.dispose();
                frmInicio frmI = new frmInicio();
                frmI.toFront();
                frmI.setVisible(true);
                frmInicio.lblIdPersona.setText(tblListado.getValueAt(0, 0).toString());
                frmInicio.lblNombre.setText(tblListado.getValueAt(0, 1).toString());
                frmInicio.lblApellidoPaterno.setText(tblListado.getValueAt(0, 2).toString());
                frmInicio.lblApellidoMaterno.setText(tblListado.getValueAt(0, 3).toString());
                frmInicio.lblAcceso.setText(tblListado.getValueAt(0, 4).toString());

                if (!frmInicio.lblAcceso.getText().equals("Administrador")) {
                    frmInicio.mnuArchivo.setEnabled(false);
                    frmInicio.mnuConfiguracion.setEnabled(false);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Acceso Denegado", "Acceso del Sistema", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

